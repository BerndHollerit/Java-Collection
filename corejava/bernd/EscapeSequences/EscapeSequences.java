public class EscapeSequences {
	public static void main(String[] args) {
		System.out.println("Backspace: \b before this");
		System.out.println("Tab: \t before this");
		System.out.println("Linefeed: \nbefore this");
		System.out.println("   Carriage return: \rCR");
		System.out.println("Double Quote: \"");
		System.out.println("Single Quote: \'");
		System.out.println("Backslash: \\");
		System.out.println("Trademark symbol: \u2122");
		System.out.println("Pi: \u03C0");
	}
}
